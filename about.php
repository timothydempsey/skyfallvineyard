      <h1>About</h1>
      
      
      <p>Skyfall Vineyard is now available in select markets with a crisp Pinot Gris, robust Merlot, and elegant Cabernet Sauvignon. The balance imparted by the near-perfect Columbia Valley growing conditions and expert winemaking make Skyfall Vineyard wines exceptionally food friendly.</p>
      <p>Skyfall Vineyard was named for massive car-sized boulders scattered among the vines, appearing to have fallen from the sky. These glacial rocks were left in the wake of the ancient Missoula floods that cut the Columbia River. Floods deposited silty loam soil mixed with volcanic ash for soil conditions that create complex, elegant wines.</p>
      <p>Dave Minick, 3rd generation Washington State grower, oversees all vineyard management for consistent, perfectly ripe grapes across Skyfall vineyard sites. All vineyards receive the same environmentally responsible care with minimalist intervention including a new compost program for naturally enriching soils and use of low impact vehicles in vineyards.</p>
      <p>Skyfall Vineyard fruit is sourced from Washington’s top AVAs within Columbia Valley including Horse Heaven Hills, Wahluke Slope, and Yakima Valley. Columbia Valley has optimal growing conditions with hot days allowing grapes to ripen and cool nights where acidity develops. This means Skyfall Vineyard wines are well balanced between the crisp acidity, firm tannins, and ripe fruit structure, ready for immediate enjoyment.</p>
      
